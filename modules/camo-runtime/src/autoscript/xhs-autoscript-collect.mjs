export function buildXhsCollectOperations(options) {
  const {
    stageLinksEnabled,
    detailLoopEnabled,
    collectIndexStart,
    collectIndexMaxAttempts,
    collectIndexFailurePolicy,
  } = options;

  return [
    {
      id: 'verify_collect_subscriptions',
      enabled: stageLinksEnabled,
      action: 'verify_subscriptions',
      params: {
        acrossPages: true,
        settleMs: 320,
        pageUrlIncludes: ['/search_result'],
        requireMatchedPages: false,
        selectors: [
          { id: 'home_search_input', selector: '#search-input, input.search-input' },
          { id: 'search_result_item', selector: '.note-item', visible: false, minCount: 1 },
        ],
      },
      trigger: 'search_result_item.exist',
      dependsOn: ['submit_search'],
      once: true,
      timeoutMs: 90000,
      onFailure: 'continue',
      impact: 'op',
    },
    {
      id: 'collect_links',
      enabled: stageLinksEnabled,
      action: 'xhs_collect_links',
      params: {
        maxNotes: options.maxNotes,
        keyword: options.keyword,
        env: options.env,
        outputRoot: options.outputRoot,
        sharedHarvestPath: options.sharedHarvestPath,
        collectOpenLinksOnly: options.collectOpenLinksOnly === true,
        collectIndexStart: collectIndexStart ?? 0,
        collectIndexMaxAttempts: collectIndexMaxAttempts ?? 3,
        collectIndexFailurePolicy: collectIndexFailurePolicy || 'retry',
      },
      trigger: 'search_result_item.exist',
      dependsOn: ['submit_search'],
      once: true,
      oncePerAppear: true,
      timeoutMs: options.collectLinksTimeoutMs || null,
      retry: { attempts: 1, backoffMs: 0 },
      validation: { mode: 'none' },
      impact: 'script',
      onFailure: 'stop_all',
      checkpoint: {
        containerId: 'xiaohongshu_search.search_result_item',
        targetCheckpoint: 'search_ready',
        recovery: options.recovery,
      },
    },
    {
      id: 'finish_after_collect_links',
      enabled: stageLinksEnabled && !detailLoopEnabled,
      action: 'raise_error',
      params: { code: 'AUTOSCRIPT_DONE_LINKS_COLLECTED' },
      trigger: 'search_result_item.exist',
      dependsOn: ['collect_links'],
      once: true,
      retry: { attempts: 1, backoffMs: 0 },
      impact: 'script',
      onFailure: 'stop_all',
      checkpoint: {
        containerId: 'xiaohongshu_search.search_result_item',
        targetCheckpoint: 'search_ready',
        recovery: options.recovery,
      },
    },
  ];
}

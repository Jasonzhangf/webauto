export function buildXhsDetailOperations(options) {
  const {
    stage,
    maxNotes,
    keyword,
    resume,
    incrementalMax,
    sharedHarvestPath,
    seedCollectCount,
    seedCollectMaxRounds,
    openDetailPreClickMinMs,
    openDetailPreClickMaxMs,
    openDetailPollDelayMinMs,
    openDetailPollDelayMaxMs,
    openDetailPostOpenMinMs,
    openDetailPostOpenMaxMs,
    detailOpenByLinks,
    openByLinksMaxAttempts,
    detailHarvestEnabled,
    commentsHarvestEnabled,
    stageLikeEnabled,
    stageReplyEnabled,
    detailLinksStartup,
    closeDependsOn,
    env,
    outputRoot,
    persistComments,
    maxComments,
    commentsScrollStepMin,
    commentsScrollStepMax,
    commentsSettleMinMs,
    commentsSettleMaxMs,
    matchMode,
    matchMinHits,
    matchKeywords,
    likeKeywords,
    maxLikesPerRound,
    replyText,
    noteIntervalMs,
    noteIntervalMinMs,
    noteIntervalMaxMs,
    tabCount,
    autoCloseDetail,
    detailRotateComments,
  } = options;
  const rotateCommentBudget = Math.max(0, Number(detailRotateComments ?? 0) || 0);
  // commentBudget = 每 N 条评论后轮转到下一个 tab（风控分散）
  // 默认: tabCount > 1 时 50 条评论轮转一次
  const defaultCommentBudget = Number(tabCount || 1) > 1 ? (rotateCommentBudget > 0 ? rotateCommentBudget : 50) : 0;
  const commentBudget = maxComments > 0 ? maxComments : defaultCommentBudget;

  const matchGateEnabled = options.matchGateEnabled === true;
  // In openByLinks mode we do not close modal per note; we only finalize link/anchor state.
  const detailFinalizeOnly = detailOpenByLinks === true;
  const detailTransitionOpId = detailFinalizeOnly ? 'finalize_detail_link' : 'close_detail';
  const detailTransitionEnabled = options.detailLoopEnabled
    && (detailFinalizeOnly ? true : autoCloseDetail !== false);
  const waitBetweenNotesDependsOn = [detailTransitionOpId];
  const openNextDependsOn = detailLinksStartup
    ? ['wait_between_notes']
    : ['wait_between_notes', 'ensure_tab_pool'];
  if (options.detailLoopEnabled && detailTransitionEnabled && Number(tabCount || 1) > 1) {
    openNextDependsOn.push('tab_switch_if_needed');
  }
  // Click-mode detail page may not render a modal container; use manual trigger to avoid subscription dependency.
  const modalChainTrigger = 'manual';
  const detailHarvestTrigger = 'manual';
  const modalChainOncePerAppear = false;
  const openNextFollowupOperations = detailOpenByLinks ? ['detail_harvest'] : [];
  const modalExistConditions = detailOpenByLinks
    ? [{ type: 'subscription_exist', subscriptionId: 'detail_modal' }]
    : undefined;
  const closeDetailConditions = detailOpenByLinks
    ? undefined
    : [
        { type: 'operation_done', operationId: closeDependsOn },
        ...(Array.isArray(modalExistConditions) ? modalExistConditions : []),
      ];

  return [
    {
      id: 'open_first_detail',
      enabled: options.detailLoopEnabled,
      action: 'xhs_open_detail',
      params: {
        mode: 'first',
        stage,
        maxNotes,
        keyword,
        env,
        outputRoot,
        resume,
        incrementalMax,
        sharedHarvestPath,
        preservePreCollected: options.stageLinksEnabled,
        seedCollectCount,
        seedCollectMaxRounds,
        preClickDelayMinMs: openDetailPreClickMinMs,
        preClickDelayMaxMs: openDetailPreClickMaxMs,
        pollDelayMinMs: openDetailPollDelayMinMs,
        pollDelayMaxMs: openDetailPollDelayMaxMs,
        postOpenDelayMinMs: openDetailPostOpenMinMs,
        postOpenDelayMaxMs: openDetailPostOpenMaxMs,
        openByLinks: detailOpenByLinks,
        openByLinksMaxAttempts,
      },
      trigger: detailLinksStartup ? 'startup' : 'search_result_item.exist',
      dependsOn: [detailLinksStartup ? 'goto_home' : (options.stageLinksEnabled ? 'wait_after_collect' : 'submit_search')],
      once: true,
      timeoutMs: 90000,
      retry: { attempts: 1, backoffMs: 0 },

      onFailure: 'continue',
      impact: 'script',
      validation: { mode: 'none' },
      checkpoint: {
        containerId: 'xiaohongshu_search.search_result_item',
        targetCheckpoint: 'detail_ready',
        recovery: options.recovery,
      },
    },
    {
      id: 'detail_harvest',
      enabled: detailHarvestEnabled,
      action: 'xhs_detail_harvest',
      params: {},
      trigger: detailHarvestTrigger,
      dependsOn: ['open_first_detail'],
      conditions: modalExistConditions,
      once: false,
      oncePerAppear: modalChainOncePerAppear,
      timeoutMs: 90000,
      retry: { attempts: 1, backoffMs: 0 },
      impact: 'op',
      onFailure: 'continue',
      pacing: { operationMinIntervalMs: 2000, eventCooldownMs: 1200, jitterMs: 260 },
      validation: { mode: 'none' },
      checkpoint: {
        containerId: 'xiaohongshu_detail.modal_shell',
        targetCheckpoint: 'detail_ready',
        recovery: options.recovery,
      },
    },
    {
      id: 'warmup_comments_context',
      enabled: commentsHarvestEnabled,
      waitFor: { mode: 'or', subscriptions: ['detail_modal', 'detail_comment_item', 'detail_comment_list', 'detail_comment_section'] },
      action: 'wait',
      params: { ms: 1200 },
      trigger: modalChainTrigger,
      dependsOn: [detailHarvestEnabled ? 'detail_harvest' : 'open_first_detail'],
      conditions: modalExistConditions,
      once: false,
      oncePerAppear: modalChainOncePerAppear,
      onFailure: 'continue',
      impact: 'op',
    },
    {
      id: 'comments_harvest',
      enabled: commentsHarvestEnabled,
      waitFor: { mode: 'or', subscriptions: ['detail_modal', 'detail_comment_item', 'detail_comment_list', 'detail_comment_section'] },
      action: 'xhs_comments_harvest',
      params: {
        tabCount,
        commentBudget,
        env,
        keyword,
        outputRoot,
        persistComments,
        doLikes: stageLikeEnabled,
        likeKeywords,
        matchMode,
        matchMinHits,
        maxLikesPerRound,
        persistLikeState: true,
        saveEvidence: true,
        commentsLimit: maxComments,
        maxRounds: 48,
        scrollStep: commentsScrollStepMax,
        scrollStepMin: commentsScrollStepMin,
        scrollStepMax: commentsScrollStepMax,
        settleMs: commentsSettleMinMs,
        settleMinMs: commentsSettleMinMs,
        settleMaxMs: commentsSettleMaxMs,
        scrollDelayMinMs: 500,
        scrollDelayMaxMs: 2000,
        stallRounds: 8,
        recoveryNoProgressRounds: 3,
        recoveryStuckRounds: 2,
        recoveryUpRounds: 3,
        recoveryDownRounds: 1,
        maxRecoveries: 3,
        adaptiveMaxRounds: true,
        adaptiveExpectedPerRound: 6,
        adaptiveBufferRounds: 22,
        adaptiveMinBoostRounds: 36,
        adaptiveMaxRoundsCap: 320,
        requireBottom: maxComments <= 0,
        includeComments: persistComments,
      },
      trigger: modalChainTrigger,
      dependsOn: ['warmup_comments_context'],
      conditions: modalExistConditions,
      once: false,
      oncePerAppear: modalChainOncePerAppear,
      disableTimeout: true,
      retry: { attempts: 1, backoffMs: 0 },
      impact: 'script',
      onFailure: 'continue',
      pacing: { operationMinIntervalMs: 2400, eventCooldownMs: 1500, jitterMs: 280 },
      validation: {
        mode: 'none',
        pre: {
          page: { hostIncludes: ['xiaohongshu.com'], checkpointIn: ['detail_ready', 'comments_ready'] },
          container: { selector: '.note-detail-mask, .note-detail-page, .note-detail-dialog', mustExist: true, minCount: 1 },
        },
        post: {
          page: { hostIncludes: ['xiaohongshu.com'], checkpointIn: ['detail_ready', 'comments_ready'] },
          container: { selector: '.comment-item, [class*="comment-item"]', mustExist: false, minCount: 0 },
        },
        onPreFailure: 'continue',
      },
      checkpoint: {
        containerId: 'xiaohongshu_detail.comment_section.comment_item',
        targetCheckpoint: 'comments_ready',
        recovery: options.recovery,
      },
    },
    {
      id: 'comment_match_gate',
      enabled: matchGateEnabled,
      action: 'xhs_comment_match',
      params: { keywords: matchKeywords, mode: matchMode, minHits: matchMinHits },
      trigger: modalChainTrigger,
      dependsOn: ['comments_harvest'],
      conditions: modalExistConditions,
      once: false,
      oncePerAppear: modalChainOncePerAppear,
      pacing: { operationMinIntervalMs: 2400, eventCooldownMs: 1200, jitterMs: 160 },
    },
    {
      id: 'comment_reply',
      enabled: stageReplyEnabled,
      action: 'xhs_comment_reply',
      params: { replyText },
      trigger: modalChainTrigger,
      dependsOn: [matchGateEnabled ? 'comment_match_gate' : 'comments_harvest'],
      conditions: modalExistConditions,
      once: false,
      oncePerAppear: modalChainOncePerAppear,
      timeoutMs: 90000,
      retry: { attempts: 1, backoffMs: 0 },
      onFailure: 'continue',
      impact: 'op',
      pacing: { operationMinIntervalMs: 2600, eventCooldownMs: 1500, jitterMs: 300 },
    },
    {
      id: detailTransitionOpId,
      enabled: detailTransitionEnabled,
      action: 'xhs_close_detail',
      params: {
        openByLinks: detailOpenByLinks,
        allowKeepDetail: false,
      },
      trigger: modalChainTrigger,
      dependsOn: [closeDependsOn],
      conditions: closeDetailConditions,
      once: false,
      oncePerAppear: modalChainOncePerAppear,
      pacing: { operationMinIntervalMs: 2500, eventCooldownMs: 1300, jitterMs: 180 },
      validation: { mode: 'none' },
      checkpoint: {
        containerId: 'xiaohongshu_detail.discover_button',
        targetCheckpoint: 'search_ready',
        recovery: options.recovery,
      },
    },
    {
      id: 'wait_between_notes',
      enabled: options.detailLoopEnabled,
      action: 'wait',
      params: options.waitBetweenNotesParams ?? { minMs: noteIntervalMinMs || 2000, maxMs: noteIntervalMaxMs || 5000, debugLabel: 'wait_between_notes' },
      // In safe-link detail mode the close -> wait chain is the single source of truth.
      // Using detail_modal.disappear here causes duplicate scheduling because the same
      // close cycle can trigger both the raw disappear event and the dependency force-run.
      trigger: detailOpenByLinks ? 'manual' : 'search_result_item.exist',
      dependsOn: waitBetweenNotesDependsOn,
      once: false,
      oncePerAppear: false,
      retry: { attempts: 1, backoffMs: 0 },
      impact: 'op',
      onFailure: 'continue',
      pacing: {
        operationMinIntervalMs: noteIntervalMinMs,
        eventCooldownMs: Math.max(400, Math.floor(noteIntervalMinMs / 2)),
        jitterMs: Math.max(0, noteIntervalMaxMs - noteIntervalMinMs),
      },
    },
    {
      id: 'tab_switch_if_needed',
      enabled: options.detailLoopEnabled && detailTransitionEnabled && Number(tabCount || 1) > 1,
      action: 'xhs_tab_switch_if_needed',
      params: {
        tabCount,
        commentBudget,
        tabSwitchDelayMinMs: 2000,
        tabSwitchDelayMaxMs: 5000,
      },
      trigger: 'manual',
      dependsOn: [detailTransitionOpId],
      once: false,
      oncePerAppear: false,
      retry: { attempts: 1, backoffMs: 0 },
      impact: 'op',
      onFailure: 'continue',
    },
    {
      id: 'open_next_detail',
      enabled: options.detailLoopEnabled,
      action: 'xhs_open_detail',
      params: {
        mode: 'next',
        stage,
        maxNotes,
        keyword,
        env,
        outputRoot,
        resume,
        incrementalMax,
        sharedHarvestPath,
        tabCount,
        preClickDelayMinMs: openDetailPreClickMinMs,
        preClickDelayMaxMs: openDetailPreClickMaxMs,
        pollDelayMinMs: openDetailPollDelayMinMs,
        pollDelayMaxMs: openDetailPollDelayMaxMs,
        postOpenDelayMinMs: openDetailPostOpenMinMs,
        postOpenDelayMaxMs: openDetailPostOpenMaxMs,
        openByLinks: detailOpenByLinks,
        openByLinksMaxAttempts,
        cleanupOnDone: detailOpenByLinks === true,
      },
      followupOperations: openNextFollowupOperations,
      // In safe-link detail mode the wait operation is the only allowed opener gate.
      // Raw disappear events would otherwise schedule a second open for the same slot.
      trigger: detailOpenByLinks ? 'manual' : 'search_result_item.exist',
      dependsOn: openNextDependsOn,
      // Safe-link detail mode keeps modal/detail context alive by design
      // (`close_detail` uses link_finalize_only and no modal close action).
      // Requiring `detail_modal not exist` here blocks the post-wait hop forever.
      // Let xhs_open_detail own the next-link decision in all openByLinks modes.
      conditions: detailOpenByLinks ? undefined : [],
      once: false,
      oncePerAppear: false,
      timeoutMs: 90000,
      retry: { attempts: 1, backoffMs: 0 },
      impact: 'op',
      onFailure: 'continue',
      checkpoint: {
        containerId: 'xiaohongshu_search.search_result_item',
        targetCheckpoint: 'detail_ready',
        recovery: { attempts: 0, actions: [] },
      },
    },
  ];
}
